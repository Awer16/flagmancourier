"""Определение района Ростова-на-Дону по координатам"""

# Приблизительные границы районов Ростова-на-Дону
# (min_lat, max_lat, min_lon, max_lon)
ROSTOV_DISTRICTS = {
    "Ворошиловский": {"lat": (47.24, 47.28), "lon": (39.68, 39.74)},
    "Советский": {"lat": (47.20, 47.24), "lon": (39.66, 39.72)},
    "Октябрьский": {"lat": (47.20, 47.24), "lon": (39.72, 39.78)},
    "Первомайский": {"lat": (47.24, 47.28), "lon": (39.74, 39.80)},
    "Ленинский": {"lat": (47.22, 47.24), "lon": (39.66, 39.72)},
    "Кировский": {"lat": (47.22, 47.24), "lon": (39.72, 39.76)},
    "Пролетарский": {"lat": (47.26, 47.30), "lon": (39.76, 39.82)},
    "Железнодорожный": {"lat": (47.23, 47.26), "lon": (39.68, 39.72)},
}


def get_rostov_district(lat: float, lon: float) -> str | None:
    """Определить район по координатам"""
    for district, bounds in ROSTOV_DISTRICTS.items():
        if (bounds["lat"][0] <= lat <= bounds["lat"][1] and
                bounds["lon"][0] <= lon <= bounds["lon"][1]):
            return district
    return None
